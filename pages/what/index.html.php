    <h1>What is Design Thinking?</h1>
    <p>
        Design thinking is an approach to problem solving that starts by gathering user data, bringing together a team and
        then methodically defining the problem, interating on a solution, and validating that solution with real users.
    </p>

    <object class="bg-gray" style="width: 100%; height: 400px; margin: 72px 0;"></object>

    <h3>Traditionally, enterprise companies approach to product design used the following approach:</h3>

    <ol class="page-steps">
        <li>
            <object class="placeholder-icon"></object>
            Ask the customer what features they want.
        </li>
        <li>
            <object class="placeholder-icon"></object>
            Pass the customer request to a business analyst who then writes up the requirements.
        </li>
        <li>
            <object class="placeholder-icon"></object>
            Hand the requirements to the developers.
        </li>
        <li>
            <object class="placeholder-icon"></object>
            Wait 6-9 months (usually longer) to deliver a beta product.
        </li>
        <li>
            <object class="placeholder-icon"></object>
            Go through weeks of QA testing and minor bug fixes.
        </li>
        <li>
            <object class="placeholder-icon"></object>
            Go to production.
        </li>
        <li>
            <object class="placeholder-icon"></object>
            When customers inevitably find problems with the product, hire support people to surface the source of the problem and bring it to the developers.
        </li>
        <li>
            <object class="placeholder-icon"></object>
            Repeat step 1.
        </li>

    </ol>


    <h3>The problem with this approach is that we’re assuming the customer always knows what they want.</h3>

    <div class="two-col">
        <p>
            What we’ve found from user interviews and extensive user testing is that, users have a difficult time expressing
            the problem; they can describe the symptoms of the problem, but without observing them in action, taking that as a
            feature list is putting band aid on the customer’s problem rather than digging into the root of the problem and
            fixing it.
        </p>

