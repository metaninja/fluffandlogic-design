        <h2 class="page-header--inside__title">
            <strong>Why Design Thinking?</strong>
        </h2>
    </header>

    <section class="page-body__section page-body__section-why">
        <div class="row">
            <div class="col">
                <h3>It’s not the user’s job to come up with a solution</h3>
                <p>
                    The problem with the traditional SDLC process is that it asks the user to tell the company what to
                    build. That’s not the user’s job! In addition, there’s been a lot of research over the decades and
                    what we found out is, what users say vs what they do are two completely different things. Users can
                    explain the symptoms of the problem, but they very rarely have the framework to suggest a solution.
                </p>
            </div>
            <div class="col">
                <h3>
                    Developing a solution to a problem requires time
                </h3>
                <p>
                    Unless I’m a product designer working on the exact same problem that I’m trying to solve, and I’m
                    incredibly passionate and focused on that exact problem, it takes time and structured thinking to
                    figure out the problem.
                </p>
                <p>
                    Beyond that, asking users for a solution only surfaces the symptom of the problem. It’s up to us,
                    as product managers, designers, researchers, and developers to dig into the symptoms and research
                    the cause, and then develop a solution.
                </p>
            </div>
        </div>

        <div>
            <h3>Solving problems requires people from different backgrounds</h3>
            <p>The cellphone. User interface. Computers. Software. UI frameworks. Computing in the cloud. All of these
                technological innovations were solutions to problems that took teams of people with different
                backgrounds to come together and iterate until they developed a viable solution.</p>
        </div>

    </section>

    <section class="page-body__conclusion">
        <h3>Saving Time With Design Thinking</h3>

        <p>
            Thankfully, most of the problems we deal with won’t take decades and hundreds or thousands of people to
            solve. Often times, we can solve it in weeks using a small team of 4-8 people. It just takes user research,
            and a structured, methodical approach to the problem.

        </p>

    </section>

